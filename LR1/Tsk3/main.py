# Запитуємо користувача ввести ціле число N
N = int(input("Введіть ціле число N (1<N<9): "))

# Перевіряємо, чи введене число відповідає обмеженням
if N <= 1 or N >= 9:
    print("Некоректне введене число N.")
else:
    # Зовнішній цикл для кількості рядків
    for i in range(1, N + 1):
        # Внутрішній цикл для виводу чисел в рядку
        for j in range(1, i + 1):
            print(j, end=' ')
        print()  # Перехід на новий рядок
