# Запитуємо користувача ввести речення
sentence = input("Введіть речення: ")

# Розбиваємо речення на слова за пробілами
words = sentence.split()

# Ініціалізуємо лічильник слів, які починаються з "н"
count_words_with_n = 0

# Перевіряємо кожне слово у реченні
for word in words:
  # Видаляємо розділові знаки та перетворюємо на малий регістр для порівняння
  cleaned_word = word.strip(".,!?").lower()
  # Перевіряємо, чи слово починається з "н"
  if cleaned_word.startswith("н"):
    count_words_with_n += 1

# Виводимо кількість слів, які починаються з "н"
print("Кількість слів, які починаються з 'н':", count_words_with_n)
